#include <iostream>

int main()
{
    // Defining variables
    int x, y, z;
    float a;

    // Initializing variables
    x = 10;
    y = 20;
    z = x + y;

    std::cout << "Value of z = " << z << std::endl;

    a = 20.0/3.0;
    std::cout << "Value of a = " << a << std::endl;

    return 0;
}